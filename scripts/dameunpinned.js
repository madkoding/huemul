const getChannelWithPinneds = () => {
  const channel = random.item(Array.from(Object.keys(pinneds)))
  return pinneds[channel].length ? channel : getChannelWithPinneds()
}

module.exports = robot => {
  robot.respond(/(dame un pinned)\s?(.*)/i, msg => {
    let channel = msg.match[2]
    if (Array.isArray(pinneds[channel])) {
      if (pinneds[channel].length > 0) {
        return msg.send(`:pushpin: https://devschile.slack.com${msg.random(pinneds[channel])}`)
      }
      return msg.send(`Canal ${channel} no tiene :pushpin: pinned`)
    } else if (channel === 'listado') {
      const messages = ['El listado de canales disponibles son:']
      for (const key in pinneds) {
        messages.push(`* *${key}*: ${pinneds[key].length} pinneds`)
      }

      return msg.send(messages.join('\n'))
    }

    channel = getChannelWithPinneds()

    return msg.send(`:pushpin: Pinned al azar canal *${channel}* https://devschile.slack.com${msg.random(pinneds[channel])}`)
  })
}
